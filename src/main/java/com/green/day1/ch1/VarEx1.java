package com.green.day1.ch1;

public class VarEx1 {
    public static void main(String[] args)  {
        int year=0;//int(데이터타입)변수, year(변수명)이라는 변수를 선언,=0(변수 초기화), pulic static void main<--(메소드명)
        System.out.println(year);//year 변수 출력.

        year=100;//변수에 새로운 값 복사.
        System.out.println(year);//year 변수 출력.

        int age=14;//int(데이터타입)변수, age(변수명)이라는 변수를 선언,=14(age라는 변수에 14를 복사).
        System.out.println(age);//age 변수 출력.

        System.out.println(age+age+age);//괄호 안 값은 사칙연산 가능.


    }
}
