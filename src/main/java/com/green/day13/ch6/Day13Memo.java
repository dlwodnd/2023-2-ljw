package com.green.day13.ch6;

public class Day13Memo {
    //static은 항상 다른 저장공간에 항상 저장 되어있다.
    //static은 객체 생성과는 상관없으며 new 를 로 불러오지 않아도 사용가능하다.
    //같은 class안에 있는 static 메소드는 class 명을 생략가능하다.
    //하지만 다른 class에서는 class 이름.스태틱 메소드명으로 사용해야한다.
    //static메소드에서는 인스턴드 변수를 사용 할 수 없으나. 인스턴트 메소드 에서는 스태틱 변수를 사용가능하다.
    //오버로딩은 매소드의 이름이 같아도 매개변수의 개수나 타입이 다르면 구현 가능한 방식중 하나다.
    //이때까지즌 메소드의 매개변수의 개수가 고정된 개수로만 사용했으나 [int ... n1]과 같이 타입 + ... + 변수명을 사용하면 가변 함수를 만들 수 있다.
    //이때 변수명은 배열로 적용된다.
}
